package String;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 123.3352f;
        System.out.printf("%.3f\n",a);
        System.out.printf("%.2f\n",a);
        System.out.printf("Pie = %.3f\n",Math.PI);
        System.out.printf("My name is %s and I am %s","Saiful","Cool");
    }
}
